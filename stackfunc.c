#include "monty.h"

/**
 * _pstr - print the string represented by the stack
 * @stack: pointer to the stack
 * @line_number: line number of the opcode
 */
void _pstr(stack_t **stack, unsigned int line_number)
{
	stack_t *runner = *stack;

	(void)line_number;

	while (runner != NULL && runner->n != 0 && _isprint(runner->n))
	{
		putchar(runner->n);
		runner = runner->next;
	}

	putchar('\n');
}

/**
 * _rotl - rotate the stack to the left
 * @stack: pointer to the stack
 * @line_number: line number of the opcode
 */
void _rotl(stack_t **stack, unsigned int line_number)
{
	stack_t *top = *stack;
	int first;

	(void)line_number;

	if (top == NULL || top->next == NULL)
		return;

	first = top->n;

	while (top->next != NULL)
	{
		top->n = top->next->n;
		top = top->next;
	}

	top->n = first;
}

/**
 * _rotr - rotate the stack to the right
 * @stack: pointer to the stack
 * @line_number: line number of the opcode
 */
void _rotr(stack_t **stack, unsigned int line_number)
{
	stack_t *top = *stack;
	int last;

	(void)line_number;

	if (top == NULL || top->next == NULL)
		return;

	while (top->next != NULL)
		top = top->next;

	last = top->n;

	while (top->prev != NULL)
	{
		top->n = top->prev->n;
		top = top->prev;
	}

	top->n = last;
}

